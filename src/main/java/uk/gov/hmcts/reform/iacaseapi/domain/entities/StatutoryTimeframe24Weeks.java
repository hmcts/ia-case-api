package uk.gov.hmcts.reform.iacaseapi.domain.entities;

import lombok.EqualsAndHashCode;
import lombok.ToString;
import uk.gov.hmcts.reform.iacaseapi.domain.entities.ccd.field.IdValue;
import uk.gov.hmcts.reform.iacaseapi.domain.entities.ccd.field.YesOrNo;

import java.util.List;

import static java.util.Objects.requireNonNull;

@EqualsAndHashCode
@ToString
public class StatutoryTimeframe24Weeks {

    private YesOrNo currentStatusAutoGenerated;
    private String currentHomeOfficeCaseTypeAutoGenerated;
    private List<IdValue<StatutoryTimeframe24WeeksHistory>> history;

    private StatutoryTimeframe24Weeks() {
    }

    public StatutoryTimeframe24Weeks(
        YesOrNo status,
        String homeOfficeCaseType,
        List<IdValue<StatutoryTimeframe24WeeksHistory>> history
    ) {
        this.currentStatusAutoGenerated = requireNonNull(status);
        this.currentHomeOfficeCaseTypeAutoGenerated = requireNonNull(homeOfficeCaseType);
        this.history = requireNonNull(history);
    }

    public YesOrNo getCurrentStatusAutoGenerated() {
        return requireNonNull(currentStatusAutoGenerated);
    }

    public String getCurrentHomeOfficeCaseTypeAutoGenerated() {
        return requireNonNull(currentHomeOfficeCaseTypeAutoGenerated);
    }

    public List<IdValue<StatutoryTimeframe24WeeksHistory>> getHistory() {
        return requireNonNull(history);
    }

}
